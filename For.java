public class For {
    public static void main(String[] args) {
       
        for(int i=1;i<=7;i++)
        {
            System.out.println("DAY"+ i);

            for(int j=9;j<=17;j++){
                System.out.println("Hour Timing "+ j +"-"+ (j+1));
            }
        }
    }
    
}
